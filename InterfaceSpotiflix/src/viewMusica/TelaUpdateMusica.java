    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //atualizar

        if (jTable1.getSelectedRow() != -1) {
            Musica music = new Musica();
            MusicaDao dao = new MusicaDao();

            music.setCod(Integer.parseInt(txtCod.getText()));
            music.setNomeCantor(txtNomeCantor.getText());
            music.setNomeMusica(txtNomeMusica.getText());
            music.setAno(Integer.parseInt(txtAno.getText()));
            
            dao.altera(music);

            txtCod.setText("");
            txtNomeCantor.setText("");
            txtNomeMusica.setText("");
            txtAno.setText("");

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            try {
                for (int i = 0; i < 5; i++) {  
                    if (dao.getList().get(i).getCod() == music.getCod()) {
                        modelo.addRow(new Object[]{
                            music.getCod(),
                            music.getNomeCantor(),
                            music.getNomeMusica(),
                            music.getAno(),
                            
                        });
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(TelaUpdateMusica.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

