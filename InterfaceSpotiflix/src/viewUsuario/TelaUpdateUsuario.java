    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //atualizar

        if (jTable1.getSelectedRow() != -1) {
            Usuario user = new Usuario();
            UsuarioDao dao = new UsuarioDao();

            user.setLogin(txtLogin.getText());
            user.setCpf(txtCpf.getText());
            user.setNome(txtNome.getText());
            user.setSenha(txtSenha.getText());
            user.setEmail(txtEmail.getText());
            user.setTelefone(txtTelefone.getText());
            user.setCidade(txtCidade.getText());
            user.setEstado(txtEstado.getText());

            dao.altera(user);

            txtLogin.setText("");
            txtCpf.setText("");
            txtNome.setText("");
            txtSenha.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
            txtCidade.setText("");
            txtEstado.setText("");

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

            try {
                for (int i = 0; i < 9; i++) {
                    if (dao.getList().get(i).getLogin().equals(user.getLogin())) {
                        modelo.addRow(new Object[]{
                            user.getNome(),
                            user.getLogin(),
                            user.getSenha(),
                            user.getEmail(),
                            user.getCpf(),
                            user.getCidade(),
                            user.getEstado(),
                            user.getTelefone()
                        });
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(TelaUpdateUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

