    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //botao procurar
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        MusicaDao dao = new MusicaDao();

        try {
            for (Musica music : dao.getList()) {
                if (music.getCod() == Integer.parseInt(procuraCod.getText())) {
                    modelo.addRow(new Object[]{
                        music.getCod(),
                        music.getNomeCantor(),
                        music.getNomeMusica(),
                        music.getAno(),
                        
                    });
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaUpdateMusica.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        //mouse clicked
        if (jTable1.getSelectedRow() != -1) {
            txtCod.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            txtNomeCantor.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            txtNomeMusica.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            txtAno.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

