    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        MusicaDao dao = new MusicaDao();
        boolean sinal = false;
        try {
            for(Musica music: dao.getList()){
                if(music.getCod() == (Integer.parseInt(txtCod.getText()))){
                    dao.remove(music);
                    sinal = true;
                }
            }
            if (sinal == false)  JOptionPane.showMessageDialog(null, "Música não encontrada!!");
        } catch (SQLException ex) {
            Logger.getLogger(TelaDeleteMusica.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro ao remover!!!");
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


